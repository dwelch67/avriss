#define UCSRA (*((volatile unsigned char *)(0xC0)))
#define UDR   (*((volatile unsigned char *)(0xC6)))

//-------------------------------------------------------------------
void uart_send ( unsigned char c )
{
    while(1) if(UCSRA&0x20) break;
    UDR=c;
}
